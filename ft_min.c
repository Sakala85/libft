#include "libft.h"

int		ft_min(int *tab, int size)
{
	int i;
	int min;

	i = 0;
	min = tab[i];
	while (i < size)
	{
		if (min > tab[i])
			min = tab[i];
		i++;
	}
	return (min);
}

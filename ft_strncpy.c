#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*s1;
	size_t	i;

	s1 = (char*)src;
	i = 0;
	while (i < len && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	while (i < len)
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}

#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*s1;
	int		i;

	s1 = (char*)src;
	i = 0;
	while (s1[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
